    public boolean readfile() throws FileNotFoundException{
        File file = new File("passenger.txt");
        Scanner sc = new Scanner(file);
        while (sc.hasNextLine()){
        String str = sc.nextLine();
        String[] lines = str.split(",");
        if (lines[2].equals(user1) && lines[3].equals(pass1)){
        line1=lines[0];
        JOptionPane.showMessageDialog(null,"Successfully Login ","Welcome User",JOptionPane.INFORMATION_MESSAGE);
        bo=true;
        break;
        }else{bo=false;}
        }
        return bo;
    }
    
